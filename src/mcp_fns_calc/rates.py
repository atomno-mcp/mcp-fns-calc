"""Встроенный снапшот налоговых ставок и лимитов РФ.

Чистый Python (без внешних зависимостей) — снапшот на дату сборки пакета.
Актуальные/свежие значения (и история ставки ЦБ для точного расчёта пеней)
доступны через hosted `get_rates` (тариф Pro). Здесь — офлайн-справочник.

Источники (сверено 2026-07):
  - Фиксвзносы ИП и кэп 1%: ст. 430 НК РФ (ред. 176-ФЗ, 259-ФЗ). nalog.ru.
  - Прогрессивная шкала НДФЛ 2025+: п. 1 ст. 224 НК РФ (176-ФЗ от 12.07.2024).
  - Лимит УСН: ст. 346.12/346.13 НК РФ, дефлятор.
Все суммы — в рублях. Каждое значение снабжено годом и источником.
"""

from __future__ import annotations

from decimal import Decimal

# Дата, на которую собран снапшот. Помечает "свежесть" офлайн-данных.
SNAPSHOT_DATE = "2026-01-01"

DISCLAIMER = (
    "Не является налоговой, бухгалтерской или юридической консультацией. "
    "Расчёт носит справочный характер; ставки и лимиты меняются — "
    "проверяйте актуальность по НК РФ и официальным сервисам ФНС/Банка России."
)

# --- Страховые взносы ИП «за себя» (ст. 430 НК РФ) -------------------------
# fixed  — фиксированная часть за полный год (доход ≤ 300 000 ₽);
# cap_1pct — предельный размер дополнительного взноса 1 % с дохода > 300 000 ₽.
IP_CONTRIBUTIONS: dict[int, dict[str, Decimal]] = {
    2024: {"fixed": Decimal("49500"), "cap_1pct": Decimal("277571")},
    2025: {"fixed": Decimal("53658"), "cap_1pct": Decimal("300888")},
    2026: {"fixed": Decimal("57390"), "cap_1pct": Decimal("321818")},
    2027: {"fixed": Decimal("61154"), "cap_1pct": Decimal("342923")},
}
IP_CONTRIB_THRESHOLD = Decimal("300000")  # порог, свыше которого +1 %

# --- НДФЛ: прогрессивная шкала с 2025 г. (п. 1 ст. 224 НК РФ) ---------------
# Список (верхняя граница ступени включительно, ставка %). Последняя ступень — inf.
NDFL_SCALE_2025: list[tuple[Decimal | None, Decimal]] = [
    (Decimal("2400000"), Decimal("13")),
    (Decimal("5000000"), Decimal("15")),
    (Decimal("20000000"), Decimal("18")),
    (Decimal("50000000"), Decimal("20")),
    (None, Decimal("22")),
]
# Плоская ставка 13 % (до 2025 / для нерезидентов-исключений оставляем справочно).
NDFL_FLAT_13 = Decimal("13")

# --- НДС (ст. 164 НК РФ) ----------------------------------------------------
VAT_RATES = {Decimal("20"), Decimal("10"), Decimal("0")}
# Спец-ставки для УСН с 2025 при доходе > порога НДС (п. 8 ст. 164 НК РФ).
VAT_USN_SPECIAL = {Decimal("5"), Decimal("7")}
VAT_USN_THRESHOLD = Decimal("60000000")  # порог дохода, с которого УСН платит НДС

# --- УСН (гл. 26.2 НК РФ) ---------------------------------------------------
USN_INCOME_RATE = Decimal("6")  # объект «доходы», базовая ставка %
USN_PROFIT_RATE = Decimal("15")  # объект «доходы минус расходы», базовая %
USN_MIN_TAX_RATE = Decimal("1")  # минимальный налог 1 % от доходов
USN_INCOME_LIMIT = {2025: Decimal("450000000"), 2026: Decimal("450000000")}

# --- ПСН (гл. 26.5 НК РФ) ---------------------------------------------------
PSN_RATE = Decimal("6")  # ставка от потенциально возможного дохода %

# --- Ключевая ставка ЦБ РФ --------------------------------------------------
# ВОЛАТИЛЬНА: для точного расчёта пеней используйте параметр key_rate или
# hosted get_rates (история по периодам). Значение ниже — только дефолт-заглушка
# офлайн-режима; ОБЯЗАТЕЛЬНО помечается как "проверьте актуальность".
CBR_KEY_RATE_DEFAULT = Decimal("21")  # % годовых (снапшот; сверять с cbr.ru)


def ip_contributions_for(year: int) -> dict[str, Decimal]:
    """Вернуть данные по взносам ИП за год; для неизвестных лет — ближайший старший."""
    if year in IP_CONTRIBUTIONS:
        return IP_CONTRIBUTIONS[year]
    known = sorted(IP_CONTRIBUTIONS)
    fallback = known[-1] if year > known[-1] else known[0]
    return IP_CONTRIBUTIONS[fallback]


def source_note(year: int) -> str:
    return f"Снапшот НК РФ на {SNAPSHOT_DATE}; год расчёта {year}. Свежие ставки — hosted get_rates."
